def how_may_tree_part_one(x: int, y: int, forest: list,char: str = '#') -> int:
    line_len = len(forest[0])
    initial_pos = [0, 0]
    completed = False
    tree = 0
    print(len(forest))
    while not completed:
        initial_pos = [(initial_pos[0] + x) % line_len, initial_pos[1] + y]
        completed = initial_pos[1] >= len(forest)
        if completed:
            break
        line = forest[initial_pos[1]]
        if line[initial_pos[0]] == char:
            tree += 1
    return tree
